    public static int findKth(int[] a, int n, int k) {
        return quickSort(a, 0, n - 1, k);
    }

    private static int quickSort(int[] a, int low, int high, int k) {
        int i = low;
        int j = high;
        int pivot = a[low];

        while (i < j) {
            while (i < j && a[j] >= pivot) {
                j--;
            }
            while (i < j && a[i] <= pivot) {
                i++;
            }
            swap(a, i, j);
        }
        swap(a, low, i);

        int bigNum = high + 1 - i;
        if (bigNum == k) {
            return pivot;
        } else if (bigNum > k) {
            return quickSort(a, i + 1, high, k);
        } else {
            return quickSort(a, low, i - 1, k - bigNum);
        }
    }

    private static void swap(int[] a, int i, int j) {
        int tmp = a[i];
        a[i] = a[j];
        a[j] = tmp;
    }
